    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String dpi = txtDPI.getText().trim();
        String nombre = txtNombre.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contrasena = txtContrasena.getText().trim();

        if (dpi.isEmpty() || nombre.isEmpty() || usuario.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        long dpiNumero = Long.parseLong(dpi);

        boolean registroExitoso = registrarseController.addClientes(usuario, contrasena, dpiNumero, nombre);
        if (registroExitoso) {
            JOptionPane.showMessageDialog(null, "Registro realizado correctamente");

            txtDPI.setText("");
            txtNombre.setText("");
            txtUsuario.setText("");
            txtContrasena.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar datos ya existentes dentro del sistema", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

